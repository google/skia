static SkPath cusp(const SkPoint P[4]) {
    SkPath path;
    path.moveTo(P[0]);
    float t = (float)frame * 2;
    if (0 <= t || t >= 1) {
        path.cubicTo(P[1], P[2], P[3]);
    } else {
        SkPoint PP[7];
        SkChopCubicAt(P, PP, t);
        path.cubicTo(PP[1], PP[2], PP[3]);
        path.cubicTo(PP[4], PP[5], PP[6]);
    }
    return path;
}

static SkScalar linearToLoop(int speed, SkScalar phase, SkScalar scale) {
    SkScalar loop;
    SkScalar linear = (time % speed) / SkIntToFloat(speed);
    linear += phase;
    if (linear > 1) {
        linear -= 1;
    }
    if (linear < .25) {
        loop = linear * 4;
    } else if (linear < .75) {
        loop = (.5 - linear) * 4;
    } else  {
        loop = (linear - 1) * 4;
    }
    return loop * scale;
}

